/**************************************************************************************************
 * 
 * \file eeprom.c
 * 
 * \brief EEPROM communication interface implementation
 * 
 * \author Marko Gulin
 * 
 *************************************************************************************************/

#include "inc/net/eeprom.h"

/**************************************************************************************************
 * 
 * eeprom_open()
 * 
 *************************************************************************************************/
int
eeprom_open(const struct net *net, const struct eeprom_usr *usr)
{
    if (usr && usr->mem) {
        return net_open(net, usr, NET_EEPROM);
    } else {
        return -1;
    }
}

/**************************************************************************************************
 * 
 * eeprom_close()
 * 
 *************************************************************************************************/
int
eeprom_close(const struct net *net)
{
    return net_close(net, NET_EEPROM);
}

/**************************************************************************************************
 * 
 * eeprom_read()
 * 
 *************************************************************************************************/
int
eeprom_read(const struct net *net, struct eeprom_f *f)
{
    return net_read(net, f, 1U, NET_EEPROM);
}

/**************************************************************************************************
 * 
 * eeprom_write()
 * 
 *************************************************************************************************/
int
eeprom_write(const struct net *net, struct eeprom_f *f)
{
    return net_write(net, f, 1U, NET_EEPROM);
}
